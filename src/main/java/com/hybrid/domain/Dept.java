package com.hybrid.domain;

public class Dept {
	int deptno;
	String dnmae;
	String loc;
	
	public int getDeptno() {
		return deptno;
	}
	public void setDeptno(int deptno) {
		this.deptno = deptno;
	}
	public String getDnmae() {
		return dnmae;
	}
	public void setDnmae(String dnmae) {
		this.dnmae = dnmae;
	}
	public String getLoc() {
		return loc;
	}
	public void setLoc(String loc) {
		this.loc = loc;
	}
	
	
}
